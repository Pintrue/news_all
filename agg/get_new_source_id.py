from news_all.db_method import DebugRW, SourceR
from news_all.tools.others import to_list


def get_new_sid(cont, env='test', maxid=150000):
    nw = SourceR(env)
    aa = nw.coll.find({}, {'source_id': 1})
    source_ids = [int(i['source_id']) for i in aa]
    
    ok = []
    count = 0
    for i in range(1, maxid):
        if i not in source_ids:
            ok.append(i)
            count += 1
            if count == cont:
                break
    return ok


def find_cat_space(env='online'):
    nw = DebugRW(env)
    aa = nw.find()
    s = []
    for i in aa:
        c = i.get('categories', [])
        if not c:
            s.append(i.get('source_id'))
    
    return s


def find_lack_mongo_source(new_ids, env='test'):
    """查找哪些source_id没有配置mongo源
    :param   new_ids list or int
    :param   env    'test' or 'online'
    :return  list    没有配置mongo源的source_id
    """
    nw = SourceR(env)
    aa = nw.coll.find({}, {'source_id': 1})
    source_ids = [int(i['source_id']) for i in aa]
    
    lacks = []
    
    for i in to_list(new_ids):
        if i not in source_ids:
            lacks.append(i)
    print('共%s个source_id, 还缺少%s个' % (len(new_ids), len(lacks)))
    return lacks


